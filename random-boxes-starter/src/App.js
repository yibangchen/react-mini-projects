import React, { Component } from 'react';
import './App.css';
import Box from './Box';

class App extends Component {
  
  constructor(props) {
    super(props);
    this.state = { 
      colors: this.initiateColors(32)
    };

    setInterval(() => { this.changeColor(); }, 300);

    this.changeColor = this.changeColor.bind(this);
  }

  initiateColors(size) {
    return Array(size).fill("").map( _ => this.getRandColor());
  }

  getRandColor() {
    return this.props.allColors[Math.floor(Math.random()*this.props.allColors.length)];
  }

  changeColor() {
    const colors = this.state.colors.slice();
    const randIndex = Math.floor(Math.random() * colors.length);
    colors[randIndex] = this.getRandColor();
    this.setState({colors});
  }

  
  render() {
    return (
      <div className="App">
        <Box colors={this.state.colors}/>
      </div>
    );
  }
}

